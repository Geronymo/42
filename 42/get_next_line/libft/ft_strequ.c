#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && k == 0)
	{
		if (s1[i] == s2[i])
			i++;
		else
			k++;
	}
	if (s1[i] == '\0' && s2[i] == '\0')
		return (1);
	else
		return (0);
}
